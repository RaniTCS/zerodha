package Multidimenstional_Array;

public class B
{
  public static void main(String[] args)
  {
	  int ar[][]= new int [3][3];
	  ar[0][1]=10;
	  ar[1][1]=20;
	  ar[1][0]=30;
	  ar[0][0]=40;
	  System.out.println(ar[1][1]);
	  for(int i=1;i<=1;i++)
	  {
		  for(int j=1;j<=1;j++)
		  {
		   System.out.print(ar[i][j]);
	      }
		  System.out.println();
	  }
  }
}
			  
	  
	  
	



package Variables;

public class Local_variable 
{
	float p=20f; 
	//GLOBAL VARIABLE (Used inside the class body and outside the method body)
	public static void main(String[] args)
	{
		int i=10; //LOCAL VARIABLE (Used inside method body and outside the class body)
		byte v=48; //Local variable
		System.out.println(i);
		System.out.println(v);
		
	}

}

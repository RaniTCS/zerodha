package PRACTICE;

public class For_loop 
{
   public static void main(String[] args)
   {
	   for(int i=1;i<=5;i++)
	   {
		   System.out.println("#");
	   }
	   for(int i=5;i<=1;i--)
	   {
		   System.out.println("*");
	   }
	   for(char ch='A';ch<='E';ch++)
	   {
		   System.out.println("@");
	   }
	   for(int i=10;i<=5;i--)
	   {
		   System.out.println("%");
	   }
	   for(char ch='Z';ch<='V';ch--)
	   {
		   System.out.println("&");
	   }
}
}

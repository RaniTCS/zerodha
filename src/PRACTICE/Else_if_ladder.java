package PRACTICE;

public class Else_if_ladder
{
	public static void main(String[] args)
	{
		int marks=92;
		if(marks>=75)
		{
			System.out.println("it will be distinction");
		}
		else if(marks==65)
		{
			System.out.println("it will be first class");
		}
		else if(marks==0)
		{
			System.out.println("it will be fail");
		}
		else
		{
			System.out.println("no such a grade");
		}
	}
	
	

}

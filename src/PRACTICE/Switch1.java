public class Switch1
{
    public static void main(String[] args)
    {
		switch("a")
		{
		case "a":
		{
			System.out.println("1st");
			break;
		}
	
		case "6":
		{
			System.out.println("1st");
			break;
		}
		default:
		{
			System.out.println("not");
		
		}
	}
}
}

package PRACTICE;

public class If_Else_statement
{
  public static void main(String[] args) 
  {
	
	  if(7!=7) //this condition is false
	  {
		  System.out.println("first condition");
	  }
	  if(6!=4)  //this condition is true therefore this if block will be executed.
	  {
		  System.out.println("second condition");
	  }
	  else                       // if block is false then this else block will be executed
	  {
		  System.out.println("false the condition");
	  }
}
}

